    private void DisplayEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DisplayEmployeeActionPerformed
        // TODO add your handling code here:
        if (userInput.getText().equals("")){
            noEmployeeFound.setVisible(true);
            EmployeeEditingPanel.setVisible(false);
            FullTimePanel.setVisible(false);
            PartTimePanel.setVisible(false);
        }
        else{
            noEmployeeFound.setVisible(false);

            int employeeNumber = Integer.parseInt(userInput.getText());
            EmployeeInfo employeeEdited = theMainHashTable.searchFromTable(employeeNumber);

            if(employeeEdited != null){

                EmployeeEditingPanel.setVisible(true);

                InputEmpNum.setText(Integer.toString(employeeEdited.getEmpNum()));
                InputFirstName.setText(employeeEdited.getFirstName());
                InputLastName.setText(employeeEdited.getLastName());
                InputDR1.setText(Double.toString(employeeEdited.getDeductRate()));
                
                
               
                if (employeeEdited.getGender() == 0){
                    MaleButton.setSelected(true);
                }

                if (employeeEdited.getGender() == 1){
                    FemaleButton.setSelected(true);
                }

                if (employeeEdited.getGender() == 2){
                    OtherButton.setSelected(true);
                }

                if (employeeEdited.getWorkLoc() == 0){
                    MississaugaButton.setSelected(true);
                }

                if (employeeEdited.getWorkLoc() == 1){
                    BramptonButton.setSelected(true);
                }

                if (employeeEdited.getWorkLoc() == 2){
                    OakvilleButton.setSelected(true);
                }

                if (employeeEdited.getWorkLoc() == 3){
                    TorontoButton.setSelected(true);
                }

                if (employeeEdited.getEmpRole() == 0){
                    HardwareButton.setSelected(true);
                }

                if (employeeEdited.getEmpRole() == 1){
                    SoftwareButton.setSelected(true);
                }

                if (employeeEdited.getEmpRole() == 2){
                    ManagerButton.setSelected(true);
                }

                if (employeeEdited instanceof FTE) {
                    EditedEmployeeFTE = (FTE) employeeEdited;
                    FullTimeButton.setSelected(true);
                    FullTimePanel.setVisible(true);
                    PartTimePanel.setVisible(false);
                    employeeAdded.setVisible(false);
                    FullTimeButton.doClick();
                    InputYearlySalary.setText(Double.toString(EditedEmployeeFTE.getYearlySalary()));
                    

                    //Resets the PTE panel incase they switch employee Type
                    InputHoursWeek.setText("");
                    InputHourWage.setText("");
                    InputWeeksYear.setText("");

                }

                if(employeeEdited instanceof PTE) {
                    EditedEmployeePTE = (PTE) employeeEdited;
                    PartTimeButton.setSelected(true);
                    PartTimePanel.setVisible(true);
                    FullTimePanel.setVisible(false);
                    employeeAdded.setVisible(false);
                 
                    PartTimeButton.doClick();
               
                    InputHoursWeek.setText(Double.toString(EditedEmployeePTE.getHourlyWage()));
                    InputHourWage.setText(Double.toString(EditedEmployeePTE.getHoursPerWeek()));
                    InputWeeksYear.setText(Double.toString(EditedEmployeePTE.getWeeksPerYear()));
                   


                    //Resets the FTE panel incase they switch employee Type
                    InputYearlySalary.setText("");
                }
            }
            else{
                //if there is no such employee in the table
                noEmployeeFound.setVisible(true);
                EmployeeEditingPanel.setVisible(false);
            }
        }
    }//GEN-LAST:event_DisplayEmployeeActionPerformed

