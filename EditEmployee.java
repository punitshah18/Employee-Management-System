    private void addEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addEmployeeActionPerformed
        // TODO add your handling code here:
        //The employee is added to the hash table
        PTE thePTE;
        FTE theFTE;
        
        int empNumRemove = Integer.parseInt(userInput.getText());
        employeeRemoved = theMainHashTable.removeEmployee(empNumRemove);
         
        //checking if the inputs using textfields are null
        if ("".equals(InputEmpNum.getText()) || "".equals(InputFirstName.getText()) || "".equals(InputLastName.getText()) || "".equals(InputDR1.getText())) {
            employeeNotAdded.setVisible(true);
        }
        else {
            employeeNotAdded.setVisible(false);

            int theNewEmployeeNumber = Integer.parseInt(InputEmpNum.getText());
            EmployeeInfo checkIfInTable = theMainHashTable.searchFromTable(theNewEmployeeNumber);

            //Input For Each Text Field
            //Input For employee number
            String employeeNumberString = InputEmpNum.getText();
            int employeeNumber = Integer.parseInt(employeeNumberString);

            //Input For first name
            String employeeFirstName = InputFirstName.getText();

            //Input For last name
            String employeeLastName = InputLastName.getText();

            //Input For Deduction Rate
            String employeeDeductString = InputDR1.getText();
            double employeeDR = Double.parseDouble(employeeDeductString);

            if (employeeType == 0) {//Assigned to add PTE
                if (checkIfInTable != null || "".equals(InputHourWage.getText()) || "".equals(InputHoursWeek.getText()) || "".equals(InputWeeksYear.getText())){ //checks if each textfield in PTE is empty
                    employeeNotAdded.setVisible(true);
                    employeeAdded.setVisible(false);
                    theMainHashTable.addEmployee(employeeRemoved);
                    employeeRemoved = null;
                }
                else {
                    employeeNotAdded.setVisible(false);

                    //Input for hour wage
                    String employeeHWstring = InputHourWage.getText();
                    double employeeHW = Double.parseDouble(employeeHWstring);

                    //Input for hours per week
                    String employeeHPWstring = InputHoursWeek.getText();
                    double employeeHPW = Double.parseDouble(employeeHPWstring);

                    //Input for weeks per year
                    String employeeWPYstring = InputWeeksYear.getText();
                    double employeeWPY = Double.parseDouble(employeeWPYstring);

                    //addes the employee given it is a PTE
                    thePTE = new PTE(employeeNumber, employeeFirstName, employeeLastName, employeeGender, employeeWorkLocation, employeeRole, employeeDR, employeeHW, employeeHPW, employeeWPY);
                    theMainHashTable.addEmployee(thePTE);
                    employeeAdded.setVisible(true);
                }
            }
            
            if (employeeType == 1){//Assigned to add FTE
                if (checkIfInTable != null || "".equals(InputYearlySalary.getText())){ //checks if each textfield in FTE is empty
                    employeeNotAdded.setVisible(true);
                    employeeAdded.setVisible(false);
                    theMainHashTable.addEmployee(employeeRemoved);
                    employeeRemoved = null;
                }
                else {
                    employeeNotAdded.setVisible(false);

                    //Input for yearly salary
                    String employeeYSstring = InputYearlySalary.getText();
                    double employeeYS = Double.parseDouble(employeeYSstring);

                    //addes the employee given it is a PTE
                    theFTE = new FTE(employeeNumber, employeeFirstName, employeeLastName, employeeGender, employeeWorkLocation, employeeRole, employeeDR, employeeYS);
                    theMainHashTable.addEmployee(theFTE);
                    employeeAdded.setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_addEmployeeActionPerformed

